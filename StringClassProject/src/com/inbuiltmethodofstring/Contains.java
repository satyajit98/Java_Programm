package com.inbuiltmethodofstring;

public class Contains {
	public static void main(String[] args) {
		String s = "I am a student of QSpider and JSpider";
		System.out.println(s.contains("student"));
		System.out.println(s.contains("qspider"));
		System.out.println(s.contains("stu"));
		System.out.println(s.contains("astudent"));
	}
}
